package cn.dtw.entity;

public class Suppliercontact{
private int supplierContactId;
private String supplierContactName;
private int supplierContactTel;
private String supplierContactEmail;
private String supplierContactQQ;

public int getSupplierContactId(){
	return supplierContactId;
}
public void setSupplierContactId(int supplierContactId){
	this.supplierContactId=supplierContactId;
}


public String getSupplierContactName(){
	return supplierContactName;
}
public void setSupplierContactName(String supplierContactName){
	this.supplierContactName=supplierContactName;
}


public int getSupplierContactTel(){
	return supplierContactTel;
}
public void setSupplierContactTel(int supplierContactTel){
	this.supplierContactTel=supplierContactTel;
}


public String getSupplierContactEmail(){
	return supplierContactEmail;
}
public void setSupplierContactEmail(String supplierContactEmail){
	this.supplierContactEmail=supplierContactEmail;
}


public String getSupplierContactQQ(){
	return supplierContactQQ;
}
public void setSupplierContactQQ(String supplierContactQQ){
	this.supplierContactQQ=supplierContactQQ;
}

}